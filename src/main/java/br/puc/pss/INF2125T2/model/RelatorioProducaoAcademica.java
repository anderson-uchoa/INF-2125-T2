package br.puc.pss.INF2125T2.model;

public class RelatorioProducaoAcademica {

	private int totalColaboradores;
	private int totalProjetos;
	private int totalProjetoEmElaboracao;
	private int totalProjetoEmAndamento;
	private int totalProjetoConcluidos;
	private int totalOrientacoes;
	private int totalPublicacoes;

	public int getTotalColaboradores() {
		return totalColaboradores;
	}

	public void setTotalColaboradores(int totalColaboradores) {
		this.totalColaboradores = totalColaboradores;
	}

	public int getTotalProjetos() {
		return totalProjetos;
	}

	public void setTotalProjetos(int totalProjetos) {
		this.totalProjetos = totalProjetos;
	}

	public int getTotalProjetoEmElaboracao() {
		return totalProjetoEmElaboracao;
	}

	public void setTotalProjetoEmElaboracao(int totalProjetoEmElaboracao) {
		this.totalProjetoEmElaboracao = totalProjetoEmElaboracao;
	}

	public int getTotalProjetoEmAndamento() {
		return totalProjetoEmAndamento;
	}

	public void setTotalProjetoEmAndamento(int totalProjetoEmAndamento) {
		this.totalProjetoEmAndamento = totalProjetoEmAndamento;
	}

	public int getTotalProjetoConcluidos() {
		return totalProjetoConcluidos;
	}

	public void setTotalProjetoConcluidos(int totalProjetoConcluidos) {
		this.totalProjetoConcluidos = totalProjetoConcluidos;
	}

	public int getTotalOrientacoes() {
		return totalOrientacoes;
	}

	public void setTotalOrientacoes(int totalOrientacoes) {
		this.totalOrientacoes = totalOrientacoes;
	}

	public int getTotalPublicacoes() {
		return totalPublicacoes;
	}

	public void setTotalPublicacoes(int totalPublicacoes) {
		this.totalPublicacoes = totalPublicacoes;
	}

}
